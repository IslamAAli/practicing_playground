bool has_cycle(SinglyLinkedListNode* head) {
    vector<SinglyLinkedListNode*> ptrs; 

    SinglyLinkedListNode* itr = head;
    while(itr != NULL)
    {
        vector<SinglyLinkedListNode*>::iterator v_itr;
        v_itr = find (ptrs.begin(), ptrs.end(), itr);
        
        if (v_itr != ptrs.end())
            return true;
        else
            ptrs.push_back(itr);

        itr = itr->next;
    }

    return false;
}

